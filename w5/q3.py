# (3) How are packages different from modules? How are modules different from functions? And how are methods different from functions?
# - Packages vs Modules:
# Packages are directories with `__init__.py` containing multiple modules; modules are single `.py` files with code.
# - Modules vs Functions:
# Modules are files containing functions, classes, etc.; functions are reusable code blocks defined with `def`.
# - Methods vs Functions:
# Methods are functions inside classes, taking `self` (instance) or `cls` (class); functions are standalone and not bound to objects.